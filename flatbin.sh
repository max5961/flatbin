#!/usr/bin/env bash

# CONSTANTS
PROXIES="./created_proxies.txt"
FILE_STAMP="### executable generated by flatbin.sh ###"
DEFAULT_BIN_LOCATION="/usr/local/bin"

# FLAGS
ARGS="${@}"
INSTALL=0
CLEAN=0
REMOVE=0
BIN_LOCATION="${DEFAULT_BIN_LOCATION}"
HELP=0

print_usage() {
    cat <<EOF
flatbin - Create executable proxies for installed Flatpak apps

Description:
    flatbin generates executables with the application name and allows for
    normal arguments to be passed.  When -v, --version is passed as an argument,
    flatbin prefixes the stdout with 'flatpak' because of the disparity between
    flatpak source version numbers and normal sources.

    Proxies are created with an internal file stamp so that flatbin will not
    overwrite existing executables found in the given path.

Usage:
    flatbin [OPTIONS]

Options:
    -i, --install           Create/update proxies [DEFAULT]
    -r, --remove            Remove generated proxies
    -c, --clean             Remove proxies from uninstalled Flatpak apps
    -l, --location=PATH     Install, remove, or clean proxies to/from PATH
                            (default: "${DEFAULT_BIN_LOCATION}")

EOF
}

parse_flags() {
    for arg in $ARGS; do
        case "${arg}" in
            --install|-i)
                INSTALL=1
                ;;
            --clean|-c)
                CLEAN=1
                ;;
            --remove|-r)
                REMOVE=1
                ;;
            --location=*|-l*)
                local bin_path=$(echo "${arg}" | awk -F '=' '{print $2}')

                if [[ "${bin_path:0:1}" == "~" ]]; then
                    bin_path="${HOME}/${bin_path:1}"
                fi

                BIN_LOCATION=$(realpath "${bin_path}")
                mkdir -p "${BIN_LOCATION}"
                ;;
            --help|-h)
                HELP=1
                ;;
        esac
    done

    # Default to running --install
    [[ $CLEAN == 1 || $REMOVE == 1 ]] && INSTALL=0
    [[ $CLEAN == 0 && $REMOVE == 0 ]] && INSTALL=1
}

# Return true if the a file already exists in /usr/local/bin under the same name
# and it was **NOT** generated by this script
non_generated_bin_already_exists() {
    if [[ -f "${app_bin}" ]]; then
        if cat "${app_bin}" | grep -E "^${FILE_STAMP}\$" >/dev/null 2>&1; then
            return 1
        fi
        return 0
    fi

    return 1
}

create_bin() {
    echo -e "#!/usr/bin/env bash\n" | sudo tee "${app_bin}" >/dev/null 2>&1
    sudo chmod +x "${app_bin}"
}

append_line_to_script() {
    local script_line="${1}"
    echo -e "${script_line}" | sudo tee -a "${app_bin}" >/dev/null 2>&1
}

get_apps() {
    flatpak list --app --columns=application
}

get_longest_app_id() {
    local longest=0

    while IFS= read -r app_id; do
        local len="${#app_id}"
        ((longest < len)) && longest=$len
    done < <(get_apps)

    echo $longest
}

install_apps() {
    printf "Installing apps to ${BIN_LOCATION}\n"
    local longest_app_id=$(get_longest_app_id)

    # Iterate installed flatpak apps
    while IFS= read -r app_id; do
        app_command=$(flatpak info --show-metadata "${app_id}" | grep '^command=' | awk -F '=' '{print $2}')
        app_bin="${BIN_LOCATION}/${app_command}"

        # Generate executable
        if non_generated_bin_already_exists; then
            printf "Skipping ${app_id}. Non-generated file already exists at ${app_bin}\n"
            continue
        fi

        create_bin
        append_line_to_script 'if [[ "${1}" == "--version" || "${1}" == "-v" ]]; then'
        append_line_to_script "\t# prefix version results with flatpak since flatpak versions may vary between sources"
        append_line_to_script "\tflatpak info ${app_id} | grep Version | awk '{print \"flatpak\", \$2}'"
        append_line_to_script "\texit 0"
        append_line_to_script "fi"
        append_line_to_script "flatpak run ${app_id} \"\${@}\""
        append_line_to_script "\n${FILE_STAMP}"

        # Log executable path for optional future removal
        if ! grep "${app_bin}" "${PROXIES}" >/dev/null 2>&1; then
            echo "${app_bin}" >> "${PROXIES}"
        fi


        ansi_len=9

        printf "%-$((longest_app_id + ansi_len + 1))b%b%b" \
            "\033[33m${app_id}\033[0m" \
            "\033[33m⇒ \033[0m" \
            "\033[32m${app_command}\033[0m\n"

    done < <(get_apps)
}

remove_apps() {
    local remaining=
    local removed_count=0

    while IFS= read -r proxy_file; do
        # Need to first create a list of installed apps by name, then verify that flatpak
        # still has them installed in order to only remove the uninstalled apps...or
        # if the flags are telling the script to remove everything, then remove everything
        # regardless

        if grep -E "^${FILE_STAMP}\$" "${proxy_file}" >/dev/null 2>&1; then
            echo "Removing ${proxy_file}"
            sudo rm "${proxy_file}"
            ((++removed_count))
        else
            remaining+="${proxy_file} "
        fi
    done < "${PROXIES}"

    printf '' > "${PROXIES}"
    for file in $remaining; do
        echo "${file}" >> "${PROXIES}"
    done

    if [[ "$removed_count" == 0 ]]; then
        echo "Nothing to remove"
    fi
}

clean_apps() {
    echo "bro"
}

main() {
    parse_flags

    if [[ $HELP == 1 ]]; then
        print_usage
        exit 0
    fi

    [[ $INSTALL == 1 ]] && install_apps
    [[ $REMOVE == 1 ]] && remove_apps
    [[ $CLEAN == 1 ]] && clean_apps
}

main
